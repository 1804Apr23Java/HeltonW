CREATE OR REPLACE FUNCTION AVG_INVOICELINE
RETURN NUMBER AS
    IVA NUMBER;
    IVB NUMBER;
BEGIN
    SELECT COUNT(*) INTO IVA FROM INVOICELINE;
    SELECT SUM(UNITPRICE) INTO IVB FROM INVOICELINE;
    RETURN IVB / IVA;
END;
/

DECLARE
    MYOUT NUMBER;
BEGIN
    MYOUT := AVG_INVOICELINE();
    DBMS_OUTPUT.PUT_LINE(MYOUT);
END;
/

