--4.1 Basic Stored Procedure
CREATE OR REPLACE PROCEDURE GET_EMPLOYEE_FIRST_LAST (EMPLOYEE_NAMES OUT SYS_REFCURSOR)
AS    
BEGIN
    OPEN EMPLOYEE_NAMES FOR
        SELECT FIRSTNAME, LASTNAME FROM EMPLOYEE;
END GET_EMPLOYEE_FIRST_LAST;
/

DECLARE
   C_EMPLOYEE SYS_REFCURSOR;
   FIRSTNAME VARCHAR2(20);
   LASTNAME VARCHAR2(20);
BEGIN
   GET_EMPLOYEE_FIRST_LAST(C_EMPLOYEE);
   LOOP
       FETCH C_EMPLOYEE INTO FIRSTNAME, LASTNAME;
       dbms_output.put_line(FIRSTNAME || ' ' || LASTNAME);
       EXIT WHEN C_EMPLOYEE%NOTFOUND;
   END LOOP;
   CLOSE C_EMPLOYEE;
END;
/

