CREATE OR REPLACE PROCEDURE GET_MANAGER(EMP_ID IN NUMBER, BOSSNAME OUT VARCHAR2)
AS
BEGIN
    SELECT (FIRSTNAME || ' ' || LASTNAME) INTO BOSSNAME FROM (SELECT * FROM EMPLOYEE WHERE EMP_ID = EMPLOYEE.EMPLOYEEID);
END;
/

DECLARE
    TEMP VARCHAR(80);
BEGIN
    GET_MANAGER(4, TEMP);
    DBMS_OUTPUT.PUT_LINE(TEMP);
END;
/

--4.3 Stored Procedure Output Parameters
CREATE OR REPLACE PROCEDURE WHOIS_CUSTOMER (CUST_ID IN NUMBER, CUST_NAME OUT VARCHAR2, COMPANY_NAME OUT VARCHAR2)
AS
BEGIN
    SELECT (FIRSTNAME || ' ' || LASTNAME) INTO CUST_NAME FROM CUSTOMER WHERE CUST_ID = CUSTOMERID;
    SELECT COMPANY INTO COMPANY_NAME FROM CUSTOMER WHERE CUST_ID = CUSTOMERID;
END;
/

DECLARE
    ID1 NUMBER := 1;
    NA VARCHAR2(80);
    CO VARCHAR2(80);
BEGIN
    WHOIS_CUSTOMER(ID1, NA, CO);    
    DBMS_OUTPUT.PUT_LINE(NA || ' ' || CO);
END;
/

